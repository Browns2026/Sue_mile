#include <iostream>
using namespace std;

int main () {
  int a = 15, b = 4;

  cout << "a + b = " << (a + b) << endl; // plus
  cout << "a - b = " << (a - b) << endl; // minus
  cout << "a * b = " << (a * b) << endl; // multiple
  cout << "a / b = " << (a / b) << endl; // division
  cout << "a % b = " << (a % b) << endl; // rest

  return 0;
}
